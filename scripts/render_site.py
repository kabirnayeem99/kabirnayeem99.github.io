#!/usr/bin/env python3
"""Render the static portfolio site from a single JSON content source.

This script keeps the published site Unix-friendly:

- Silent success by default.
- Non-zero exit codes on schema or render failures.
- `--check` prints only stale output paths to stdout and exits 1 when any file
  is out of date.
- Diagnostics go to stderr.
- `--content -` reads JSON from stdin, which makes the tool composable in
  pipelines.

The renderer is intentionally strict. It parses JSON into typed, immutable
dataclasses first and only renders after the schema is validated.

The output side follows a small build-pipeline model:

- HTML pages are rendered from structured JSON and then minified.
- CSS is authored in `assets/css/styles.source.css` and emitted as minified
  `assets/css/styles.css`.
"""

