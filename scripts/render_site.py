@dataclass(frozen=True, slots=True)
class SiteContent:
    """Fully parsed and typed site content."""

    site: SiteSettings
    routes: RouteTable
    navigation: NavigationTable
    navigation_labels: NavigationLabels
    index_page: LocalizedPage[IndexPageLocale]
    work_page: LocalizedPage[WorkPageLocale]
    project_page: LocalizedPage[ProjectPageLocale]
    blog_page: LocalizedPage[BlogPageLocale]
    stats_page: LocalizedPage[StatsPageLocale]

    def iter_pages(
        self,
        page_filter: frozenset[PageId] | None = None,
        lang_filter: frozenset[Lang] | None = None,
    ) -> Iterable[tuple[PageId, Lang, str, str]]:
        """Yield page_id, lang, output path, and rendered HTML."""

        for page_id in PAGE_IDS:
            if page_filter is not None and page_id not in page_filter:
                continue
            localized_page = self.page_for(page_id)
            route_map = self.routes[page_id]
            for lang, route in route_map.items():
                if lang_filter is not None and lang not in lang_filter:
                    continue
                yield (
                    page_id,
                    lang,
                    route,
                    render_page(self, page_id, lang, route, localized_page.og_type),
                )

    def page_for(
        self,
        page_id: PageId,
    ) -> (
        LocalizedPage[IndexPageLocale]
        | LocalizedPage[WorkPageLocale]
        | LocalizedPage[ProjectPageLocale]
        | LocalizedPage[BlogPageLocale]
        | LocalizedPage[StatsPageLocale]
    ):
        """Return the typed page descriptor for a page id."""

        if page_id == "index":
            return self.index_page
        if page_id == "work":
            return self.work_page
        if page_id == "project":
            return self.project_page
        if page_id == "blog":
            return self.blog_page
        return self.stats_page


