def git_output(root: Path, *args: str) -> str | None:
    """Return trimmed git command output, or None when unavailable."""

    try:
        completed = subprocess.run(
            ("git", *args),
            cwd=root,
            check=True,
            capture_output=True,
            text=True,
        )
    except (OSError, subprocess.CalledProcessError):
        return None
    return completed.stdout.strip() or None


def github_repo_slug(remote_url: str) -> str | None:
    """Extract owner/repo from a GitHub remote URL."""

    normalized = remote_url.removesuffix(".git")
    if normalized.startswith("https://github.com/"):
        return normalized.removeprefix("https://github.com/")
    if normalized.startswith("git@github.com:"):
        return normalized.removeprefix("git@github.com:")
    return None


def eb_garamond_woff2_url(root: Path) -> str:
    """Return a cache-friendly WOFF2 URL with a safe local fallback."""

    local_path = "../fonts/eb-garamond/EBGaramond-wght.woff2"
    remote_url = git_output(root, "config", "--get", "remote.origin.url")
    commit_hash = git_output(root, "rev-parse", "HEAD")
    if remote_url is None or commit_hash is None:
        return local_path

    repo_slug = github_repo_slug(remote_url)
    if repo_slug is None:
        return local_path

    return f"https://cdn.jsdelivr.net/gh/{repo_slug}@{commit_hash}/assets/fonts/eb-garamond/EBGaramond-wght.woff2"


