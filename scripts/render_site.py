def read_content_stream(content_path: str, stdin: TextIO) -> str:
    """Read JSON from a file path or stdin when the path is '-'."""

    if content_path == "-":
        return stdin.read()
    return Path(content_path).read_text(encoding="utf-8")


def load_site_content(content_path: str, stdin: TextIO) -> SiteContent:
    """Load and validate the content source."""

    raw_text = read_content_stream(content_path, stdin)
    try:
        raw_value = cast(object, json.loads(raw_text))
    except json.JSONDecodeError as error:
        raise ContentSchemaError(f"invalid JSON: {error.msg} at line {error.lineno}, column {error.colno}") from error
    return parse_content_source(raw_value)


def strip_css_comments(source: str) -> str:
    """Remove block comments from CSS while preserving string literals.

    The implementation is intentionally conservative and only understands the
    CSS constructs used in this repository. It avoids regex-only comment
    stripping so quoted `/* ... */` sequences remain intact.
    """

    result: list[str] = []
    index = 0
    in_string = False
    string_delimiter = ""
    length = len(source)
    while index < length:
        current = source[index]
        next_char = source[index + 1] if index + 1 < length else ""
        if in_string:
            result.append(current)
            if current == "\\" and index + 1 < length:
                result.append(source[index + 1])
                index += 2
                continue
            if current == string_delimiter:
                in_string = False
                string_delimiter = ""
            index += 1
            continue
        if current in ('"', "'"):
            in_string = True
            string_delimiter = current
            result.append(current)
            index += 1
            continue
        if current == "/" and next_char == "*":
            comment_end = source.find("*/", index + 2)
            if comment_end == -1:
                raise ContentSchemaError("assets/css/styles.source.css contains an unterminated CSS comment")
            index = comment_end + 2
            continue
        result.append(current)
        index += 1
    return "".join(result)


def collapse_css_whitespace(source: str) -> str:
    """Collapse runs of CSS whitespace outside string literals to one space."""

    result: list[str] = []
    in_string = False
    string_delimiter = ""
    pending_space = False
    index = 0
    length = len(source)
    while index < length:
        current = source[index]
        if in_string:
            if pending_space:
                result.append(" ")
                pending_space = False
            result.append(current)
            if current == "\\" and index + 1 < length:
                result.append(source[index + 1])
                index += 2
                continue
            if current == string_delimiter:
                in_string = False
                string_delimiter = ""
            index += 1
            continue
        if current in ('"', "'"):
            if pending_space:
                result.append(" ")
                pending_space = False
            in_string = True
            string_delimiter = current
            result.append(current)
            index += 1
            continue
        if current.isspace():
            pending_space = True
            index += 1
            continue
        if pending_space and result:
            result.append(" ")
            pending_space = False
        result.append(current)
        index += 1
    return "".join(result).strip()


def minify_css(source: str) -> str:
    """Produce a compact CSS payload from the readable source stylesheet."""

    collapsed = collapse_css_whitespace(strip_css_comments(source))
    minified = re.sub(r"\s*([{}:;,])\s*", r"\1", collapsed)
    minified = re.sub(r"\)\s+\{", "){", minified)
    minified = re.sub(r"\s*>\s*", ">", minified)
    minified = re.sub(r";}", "}", minified)
    return minified.strip() + "\n"


def minify_html_document(document: str) -> str:
    """Minify generated HTML without altering text-node semantics.

    The renderer only strips whitespace between tags and removes the generated
    build comment. It does not rewrite text content, attribute values, or inline
    HTML fragments coming from the content source.
    """

    without_comment = document.replace(f"{GENERATED_COMMENT}\n", "")
    return re.sub(r">\s+<", "><", without_comment).strip() + "\n"


def relative_href(from_output: str, to_output: str) -> str:
    """Compute a POSIX relative URL between two output paths."""

    from_dir = posixpath.dirname(from_output) or "."
    return posixpath.relpath(to_output, start=from_dir)


def route_for(routes: RouteTable, page_id: PageId, lang: Lang) -> str:
    """Resolve a route for a given page and locale, falling back to localized home."""

    page_routes = routes[page_id]
    if lang in page_routes:
        return page_routes[lang]
    return routes["index"][lang]


def canonical_url(base_url: str, route: str, page_id: PageId, lang: Lang) -> str:
    """Build the canonical URL for a rendered page."""

    if page_id == "index" and lang == "en":
        return f"{base_url}/"
    return f"{base_url}/{route}"


def alternate_language_links(site: SiteSettings, routes: RouteTable, page_id: PageId) -> tuple[str, ...]:
    """Build alternate-language link tags for the actual page variants only."""

    page_routes = routes[page_id]
    alternate_links: list[str] = []

    for alternate_lang in LANGS:
        if alternate_lang not in page_routes:
            continue
        alternate_href = canonical_url(site.base_url, page_routes[alternate_lang], page_id, alternate_lang)
        alternate_links.append(
            f'  <link rel="alternate" hreflang="{alternate_lang}" href="{html.escape(alternate_href)}" />'
        )

    default_lang = "en" if "en" in page_routes else next(iter(page_routes.keys()))
    default_href = canonical_url(site.base_url, page_routes[default_lang], page_id, default_lang)
    alternate_links.append(
        f'  <link rel="alternate" hreflang="x-default" href="{html.escape(default_href)}" />'
    )

    return tuple(alternate_links)


