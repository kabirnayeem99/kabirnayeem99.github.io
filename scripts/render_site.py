def inline_script_tag(script_path: str) -> str:
    """Inline a local script file into the generated HTML."""

    source_path = DEFAULT_ROOT / script_path
    script_source = source_path.read_text(encoding="utf-8")
    safe_source = script_source.replace("</script", "<\\/script")
    return f"  <script>{safe_source}</script>"


def render_scripts(page_id: PageId, current_output: str) -> str:
    """Render deferred script tags for a page."""

    default_scripts = (
        "assets/js/image-guard.js",
        "assets/js/theme-toggle.js",
        "assets/js/language-switcher.js",
        "assets/js/year.js",
    )
    long_page_scripts = (
        "assets/js/image-guard.js",
        "assets/js/back-to-top.js",
        "assets/js/theme-toggle.js",
        "assets/js/language-switcher.js",
        "assets/js/year.js",
    )
    stats_scripts = (
        "assets/js/image-guard.js",
        "assets/js/theme-toggle.js",
        "assets/js/stats-theme-embeds.js",
        "assets/js/language-switcher.js",
        "assets/js/stats-snapshots.js",
        "assets/js/stats-utils.js",
        "assets/js/wakatime-charts.js",
        "assets/js/github-commits.js",
        "assets/js/year.js",
    )
    if page_id == "stats":
        paths = stats_scripts
    elif page_id in ("work", "project"):
        paths = long_page_scripts
    else:
        paths = default_scripts
